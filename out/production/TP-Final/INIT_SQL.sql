
DROP TABLE IF EXISTS EMPLOYEE;



CREATE TABLE COMPANY (ID INT PRIMARY KEY, NAME VARCHAR(30));
CREATE TABLE PROJECT (ID INT PRIMARY KEY, NAME VARCHAR(30), COMPANY_ID INT,
                      FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY(ID));
CREATE TABLE EMPLOYEE (ID INT PRIMARY KEY, NAME VARCHAR(30),
                       LAST_NAME VARCHAR(30), RATE_PER_HOUR INT,PROJECT_ID INT,
                       FOREIGN KEY (PROJECT_ID) REFERENCES PROJECT(ID));
CREATE TABLE TASK (ID INT PRIMARY KEY, TITLE VARCHAR(30), DESCRIPTION VARCHAR(100),
                    ESTIMATION INT, ASSIGNED INT,
                    FOREIGN  KEY (ASSIGNED) REFERENCES EMPLOYEE(ID),
                    PROJECT_ID INT,
                    FOREIGN KEY (PROJECT_ID) REFERENCES PROJECT(ID));
CREATE TABLE EVENT_TASK (ID INT PRIMARY KEY, EMPLOYEE_ID INT,
                         FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE(ID), TASK_ID INT,
                         FOREIGN KEY (TASK_ID) REFERENCES TASK(ID), ACTION VARCHAR(30));
